package vo;

public class Paging {
	private int pageNum;
	private Boolean pageSelect;
	
	public int getPageNum() {
		return pageNum;
	}
	public void setPageNum(int pageNum) {
		this.pageNum = pageNum;
	}
	public Boolean getPageSelect() {
		return pageSelect;
	}
	public void setPageSelect(Boolean pageSelect) {
		this.pageSelect = pageSelect;
	}
}

